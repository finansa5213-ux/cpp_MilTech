#pragma once
// ============================================================
// ITargetProvider — абстрактний інтерфейс провайдера цілей.
//
// Призначення: відокремити "ЩО таке ціль і де її взяти" від
// решти системи. MissionProcessor працює лише з цим інтерфейсом
// і не знає, чи цілі прийшли з JSON-файлу, з бінарного потоку,
// з мережі чи з тестового стенду.
//
// Кожна реалізація (JsonTargetProvider, SerialTargetProvider, ...)
// сама визначає, як завантажити дані. Інтерфейс гарантує лише
// дві операції: дізнатись кількість цілей і отримати ціль за
// індексом.
// ============================================================

#include "Types.h"

class ITargetProvider {
public:
    // Скільки цілей доступно. Стабільно протягом життя об'єкта.
    virtual int    getTargetCount()       = 0;

    // Дані конкретної цілі. Індекс має бути в [0, getTargetCount()).
    // Реалізація сама вирішує, чи це поточна позиція, прогноз тощо.
    virtual Target getTarget(int index)   = 0;

    // virtual деструктор — обов'язковий для поліморфних класів,
    // інакше delete через вказівник на базовий клас не викличе
    // деструктор похідного → витоки.
    virtual ~ITargetProvider() {}
};
